// دالة لتنظيف الأسماء وتوحيدها بدون همزات عشان الأعضاء ميزعلوش
function cleanName(str) {
    return str.trim()
        .replace(/[أإآا]/g, 'ا')
        .replace(/ة/g, 'ه')
        .replace(/[ىي]/g, 'ي')
        .toLowerCase();
}

async function startDetectiveTimeout(chatId, conn) {
    const game = global.detectiveGame[chatId];
    if (!game) return;
    if (game.timer) clearTimeout(game.timer);

    game.timer = setTimeout(async () => {
        if (global.detectiveGame?.[chatId]) {
            delete global.detectiveGame[chatId];
            await conn.sendMessage(chatId, {
                text: `💤 *يا خسارة! الجروب نام ومحدش عرف يخمن الشخصية..* 🦦\n*تم إلغاء جولة المحقق لعدم التفاعل.*\n\n> 💡 _اللي حابب يصحصح الجروب يكتب 👈🏻 .تخمين عشان يبدأ من جديد!_`
            });
        }
    }, 35000); // 35 ثانية لو محدش جاوب يقفل تلقائي
}

async function nextRound(m, conn, chatId) {
    const game = global.detectiveGame[chatId];
    if (!game) return;

    game.round++;
    if (game.round > MAX_ROUNDS) {
        return finishDetectiveGame(m, conn, chatId);
    }

    // اختيار الشخصية الحالية بناءً على المصفوفة المخلوطة لضمان عدم التكرار
    game.currentQuestion = game.questionsPool[game.round - 1];

    const currentCharacter = game.currentQuestion;
    const msgText = `🕵🏻‍♂️ *حَلَبَةْ الـمُـحَـقِّـقْ الْأَسْـطُـورِيَّــة* 🔍\n\nجولة رقم: [ *${game.round} من ${MAX_ROUNDS}* ] 🎲\n\n💡 *إليك 3 تلميحات سرية ومظبوطة عن الشخصية:*\n\n1️⃣ ⎋ ${currentCharacter.hints[0]}\n2️⃣ ⎋ ${currentCharacter.hints[1]}\n3️⃣ ⎋ ${currentCharacter.hints[2]}\n\n✨ *الشات مفتوح للجميع! مين أول واحد هيكتشف اسم الشخصية ويكسب الجولة؟* 🚀💥`;

    const sent = await conn.sendMessage(chatId, { text: msgText });
    game.lastMsgId = sent.key.id;

    startDetectiveTimeout(chatId, conn);
}

